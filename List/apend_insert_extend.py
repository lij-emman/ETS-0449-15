print("APPEND()")
BibleNames = ['Solomon', 'David', 'Ruth']
BibleNames.append('Aster')
print(BibleNames)

print("EXTEND()")
BibleNames = ['Solomon', 'David', 'Ruth']
NewTestament = ['Peter', 'Paul', 'Jesus']
BibleNames.extend(NewTestament)
print(BibleNames)

print("INSERT()")
BibleNames = ['Solomon', 'David', 'Ruth']
BibleNames.insert(2, 'Abraham')
print(BibleNames)