print("CLEAR()")
BibleNames = ['Solomon', 'David', 'Ruth']
print(BibleNames.clear())


print("POP()")
BibleNames = ['Solomon', 'David', 'Ruth']
print(BibleNames.pop())
print(BibleNames)

BibleNames = ['Solomon', 'David', 'Ruth']
print(BibleNames.pop(0))
print(BibleNames)


print("REMOVE()")
BibleNames = ['Solomon', 'David', 'Ruth', 'Ruth']
BibleNames.remove('Ruth')
print(BibleNames)